Православната църква празнува на този ден <a href="https://bg.wikipedia.org/wiki/%D0%91%D0%BB%D0%B0%D0%B3%D0%BE%D0%B2%D0%B5%D1%89%D0%B5%D0%BD%D0%B8%D0%B5">Благовещение</a> (празник наричан още <i>Благовец</i>).
<br/><br/>
Предполагаем първообраз на обредността на този ден е денят на пролетното равноденствие, 
който при Древния Български Календар се е падал винаги точно на първия ден от второто 
тримесечие от Древния Български Календар(01-04).

